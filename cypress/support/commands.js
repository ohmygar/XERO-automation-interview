import * as productPage from "../support/pageObjects/productPage";

Cypress.Commands.add('addToCart', (itemName) => {
    //add any item into the cart by name
    productPage.checkout(itemName);
    //verify the cart badge appears
    productPage.getCartBadge().should('exist');
})

